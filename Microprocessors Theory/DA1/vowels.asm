 ASSUME CS:CODE,DS:DATA
   DATA SEGMENT
  STRING DB "My name is Aryaman Mishra$"
 DATA ENDS
  CODE SEGMENT
  START:
  MOV AX, DATA
  MOV DS, AX
  XOR AX,AX
  XOR BX,BX
 BACK: MOV AL, [SI]
  CMP AL,'$'
  JZ FINAL
  CMP AL,'A'
  JZ COUNT   
  CMP AL,'E'
  JZ COUNT   
  CMP AL,'I'
  JZ COUNT   
  CMP AL,'O'
  JZ COUNT   
  CMP AL,'U' 
  JZ COUNT
  CMP AL,'a'
  JZ COUNT   
  CMP AL,'e'
  JZ COUNT   
  CMP AL,'i'
  JZ COUNT   
  CMP AL,'o'
  JZ COUNT   
  CMP AL,'u'
  JZ COUNT   
  INC SI
  JMP BACK 
  COUNT: INC BL
  INC SI
  JMP BACK
  FINAL: MOV AH, 4CH
  INT 3
  CODE ENDS
  END START

  